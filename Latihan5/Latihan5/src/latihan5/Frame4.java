    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        /**
         * Membuat variabel untuk jTextField dari urutan ke 2 hingga ke 4
         * 
         * yang nantinya variabel ini dipakai di preparedstatement
         */
        
        String namaLengkap = jTextField2.getText();
        
        String namaPengguna = jTextField3.getText();
        
        String kataKunci = jTextField4.getText();
        
        /**
         * Membuat nilai Koneksi dan PreparedStatement yg dalam hal ini diwakili
         * 
         * oleh PSInsert menjadi null
         * 
         */
        
        Connection Koneksi = null;
        
        PreparedStatement PSInsert = null;
        
        
        /**
         * Membuat perintah SQL dengan format string untuk memasukan data ke
         * 
         * Database PostgreSQL
         */
        
        
        String masukanData = "INSERT INTO pengguna" + "(namalengkap,namapengguna,katakunci) VALUES" + "(?,?,?)";
        
        
        try {
            
            
            String url = "jdbc:postgresql://localhost:5432/pdam";
            
            String user = "steven";
            
            String password = "kucing";
            
            
            Koneksi = DriverManager.getConnection(url,user,password);
            
            
            Koneksi.setAutoCommit(false);
            
            
            if(Koneksi  != null){
            
            
            
            PSInsert = Koneksi.prepareStatement(masukanData);
            
            
            PSInsert.setString(1, namaLengkap);
            
            PSInsert.setString(2, namaPengguna);
            
            PSInsert.setString(3, kataKunci);
            
            
            
            PSInsert.executeUpdate();
            
            Koneksi.commit();
            
            
            Koneksi.close();
            
            jTextField1.setText("");
            
            jTextField2.setText("");
            
            jTextField3.setText("");
            
            jTextField4.setText("");
            
            
        }
        
        
    } catch (SQLException ex) {
    
    
    
    
            System.out.println();
            
            ex.printStackTrace();
            
            
}
            
            
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

