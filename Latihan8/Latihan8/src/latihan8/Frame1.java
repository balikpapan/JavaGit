    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        // Bikin default tabel model dulu yg namanya tm
        DefaultTableModel tm = (DefaultTableModel)jTable1.getModel();
        
        
        if (Editable == true)
        
        {
            
            
            int editData = jTable1.getSelectedRow();
            
            
           // java.sql.Date tanggal1 = new java.sql.Date(jDateChooser1.getDate().getTime());
            
          //  java.sql.Date tanggal2 = new java.sql.Date(jDateChooser2.getDate().getTime());
            
            String tanggal1 = jDateChooser1.getDate().toString();
            
            String tanggal2 = jDateChooser2.getDate().toString();
            
            
            tm.setValueAt(new String (tanggal1), editData, 0);
            
            tm.setValueAt(new String (tanggal2), editData, 1);
            
            
            Editable = false;
            
        }
                
        
        else
        
            {
                
                try {
                
                /* Mengkonversi nilai tanggal yg ada di JDateChooser1 ke format string
                sehingga bisa ditampilkan di JTable
                    */
                String tanggal1 = jDateChooser1.getDate().toString();
                
                /* Mengkonversi nilai tanggal yg ada di JDateChooser2 ke format string
                sehingga bisa ditampilkan di JTable
                    */
                
                String tanggal2 = jDateChooser2.getDate().toString();
                
                
                // Memasukan nilai yg sudah dalam wujud string ke JTable
                tm.addRow(new Object[]{new String(tanggal1), new String(tanggal2)});
                
                
                jTable1.setModel(tm);
                
                // buat menangkap kalau terjadi error, biar ketahuan error nya karena apa
                
                } catch (Exception ex){
                
                
                }
            
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

